a = 10
b = 3

print(a + b) # գումարում
print(a - b) # հանում
print(a*b) # բազմապատկում
print(a/b) # բաժանում
print(a//b) # բաժանում, և վերադարձնում է թվի ամբողջ մասը, 10//3=3
print(a%b) # բաժանում, և վերադարձնում է թվի մնացորդային մասը, 10%3=10-(3*(10//3))=1
print(a**b) # աստիճանի բարձրացում, 10**3=1000